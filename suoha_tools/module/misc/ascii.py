# ascii转字符串

def Ascii():
    try:
        print('——————————————————————————————————————————————————————————————————————————————')
        print('选择了倒序转换')
        print('——————————————————————————————————————————————————————————————————————————————')
        d = input('请输入待转换的ascii码:')
        print('——————————————————————————————————————————————————————————————————————————————')
        d_list = d.split(" ")

        char_list = [chr(int(i)) for i in d_list]
        for i in char_list:
            print(i, end="")
        print("/n")
        print('——————————————————————————————————————————————————————————————————————————————')

    except:
        print('请输入正确的数据!')
